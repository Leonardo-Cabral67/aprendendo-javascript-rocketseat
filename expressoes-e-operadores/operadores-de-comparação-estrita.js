// Estritamente diferente e estritamente igual

// O primeiro == se refere ao valor recebido e === se refere ao valor e ao tipo 

let one = 1
let two = 2

// Estritamente igual  ===
console.log( one === "1" )
console.log( one === 1 )

// Estritamente diferente 
console.log( two !== 2 )
console.log( two !== "2")
