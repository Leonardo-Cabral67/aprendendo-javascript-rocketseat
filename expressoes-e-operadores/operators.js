// Operators
/*
* Unary
    Consisite em um operador que tem apenas um elemento à sua frente, que so interage com 1.
*Binary
    Consiste em um operador que interage com dois elementos.
*Ternary
    Consiste em um operador que interage com três elementos.
*/

let number1 = 7
let number2 = 3

console.log(++number1) // Esse é um unary operator, nesse caso é um operador de incremento

console.log(number1 + number2) // Esse é um binary operator, nesse caso é um operador aritmético de soma 

console.log(false? 'alo' : 'nada') // Esse é um ternary operador